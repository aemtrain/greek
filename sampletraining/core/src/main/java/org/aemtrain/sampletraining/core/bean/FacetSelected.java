package org.aemtrain.sampletraining.core.bean;

public class FacetSelected {
	
	private String selectedFilter;
	private String filterTitleLabel;
	private String filterResetLabel;
	private String filterMoreLabel;
	
	public String getSelectedFilter() {
		return selectedFilter;
	}
	public void setSelectedFilter(String selectedFilter) {
		this.selectedFilter = selectedFilter;
	}
	public String getFilterTitleLabel() {
		return filterTitleLabel;
	}
	public void setFilterTitleLabel(String filterTitleLabel) {
		this.filterTitleLabel = filterTitleLabel;
	}
	public String getFilterResetLabel() {
		return filterResetLabel;
	}
	public void setFilterResetLabel(String filterResetLabel) {
		this.filterResetLabel = filterResetLabel;
	}
	public String getFilterMoreLabel() {
		return filterMoreLabel;
	}
	public void setFilterMoreLabel(String filterMoreLabel) {
		this.filterMoreLabel = filterMoreLabel;
	}
	

}
